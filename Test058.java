// ○ 과제
// 다음과 같은 내용이 출력될 수 있도록
// 반복문의 중첩 구문을 작성하여 프로그램을 구현한다.

// 실행 예)
/*

*********
 *******
  *****
   ***
   	*

*/
public class Test058
{
	public static void main(String[] args)
	{
	// 주요 변수 선언
	int i, j, k;

	// 연산 및 처리 + 결과 출력

		for(i=1; i<=5; i++) 
		{
			for(j=1; j<i; j++)
				System.out.print(" ");	// 공백 출력

			for(k=9; k>=2*i-1; k--)
				System.out.print("*");  // 별 출력

			System.out.println();	
		}
	}

}


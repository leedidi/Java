public class Ingredient
{
	public String name;	// 재료명
	public int num;		// 수량
	public int kcal;		// 칼로리
	public int money;		// 금액

	public Ingredient(String name, int num, int kcal, int money)
	{
		this.name = name;	// 재료명
		this.num = num;		// 수량
		this.kcal = kcal;		// 칼로리
		this.money = money;		// 금액
	}

}